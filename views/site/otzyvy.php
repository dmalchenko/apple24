                    var $wrap = $('#ajax-wrap');
                    $(this).parent().addClass('active').siblings().removeClass('active');
                    $wrap.html('').slideUp().load(href, function () {
                        setTimeout(function () {
                            document.getElementById('load').style.visibility = "hidden";
                            $wrap.slideDown();
                        }, 1000);
                    });
                });

